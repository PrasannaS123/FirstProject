package org.firstassignment;

public class Switch {

	public static void main(String[] args) {
		int month = 10;
		switch (month) {
		case 1:
			System.out.println("may");
			break;
		case 2:
			System.out.println("july");
			break;
		case 3:
			System.out.println("septembar");
			break;

		default:
			System.out.println("june");
			break;
		}
	}

}
